fun getGitCommitCount(): Int {
    val process = ProcessBuilder("git", "rev-list", "--count", "HEAD").start()
    val count = process.inputStream.bufferedReader().readText().trim()
    process.waitFor()
    return if (count.isNotEmpty()) count.toInt() else 1
}
